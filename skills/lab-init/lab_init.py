def write_graph(cfg: dict, project: dict | None = None):
    GRAPH_FILE.parent.mkdir(parents=True, exist_ok=True)
    meta = {"type": "Meta", "schema_version": "0.2",
            "created": now_iso(), "owner": cfg["user"]}
    with open(GRAPH_FILE, "w") as f:
        f.write(json.dumps(meta) + "\n")
    if project:
        _append_project_node(project)
    print("  ✅ research-graph.jsonl")


def _append_project_node(project: dict):
    slug   = slugify(project["name"])
    hyps   = [project["hypothesis"]] if project.get("hypothesis") else []
    node   = {
        "type": "Project", "id": f"proj_{slug}",
        "name": project["name"], "description": project["description"],
        "status": project["status"], "hypotheses": hyps,
        "experiments": [], "drafts": [],
        "created": now_iso(), "updated": now_iso(),
    }
    with open(GRAPH_FILE, "a") as f:
        f.write(json.dumps(node) + "\n")


def scaffold_obsidian(vault_path: str, project: dict | None = None) -> bool:
    vault = Path(vault_path)
    if not vault.exists():
        try:
            vault.mkdir(parents=True)
            (vault / ".obsidian").mkdir(exist_ok=True)
            (vault / ".obsidian" / "app.json").write_text("{}")
            print(f"  ✅ New Obsidian vault created at {vault_path}")
        except Exception as e:
            print(f"  ⚠️  Could not create vault: {e}")
            return False

    for folder in ["Research/Projects", "Research/Literature",
                   "Research/Methods", "Research/Weekly-Digests"]:
        (vault / folder).mkdir(parents=True, exist_ok=True)

    (vault / "Research" / "README.md").write_text(
        "# Research Vault\nManaged by LabOS.\n\n"
        "- **Projects/** — notes, hypotheses, drafts per project\n"
        "- **Literature/** — paper summaries from lab-lit-scout\n"
        "- **Methods/** — protocol notes, stats tips\n"
        "- **Weekly-Digests/** — field trend digests\n"
    )

    if project:
        slug     = slugify(project["name"])
        proj_dir = vault / "Research" / "Projects" / slug
        proj_dir.mkdir(parents=True, exist_ok=True)
        (proj_dir / "hypotheses.md").write_text(
            f"# Hypotheses — {project['name']}\n\n"
            + (f"- {project['hypothesis']}\n" if project.get("hypothesis") else "")
        )
        (proj_dir / "notes.md").write_text(
            f"# Notes — {project['name']}\n\n{project.get('description','')}\n"
        )
        (proj_dir / "drafts").mkdir(exist_ok=True)
        print(f"  ✅ Obsidian project folder: Research/Projects/{slug}/")

    print(f"  ✅ Obsidian vault scaffolded")
    return True


def register_cron(cfg: dict):
    day_map = {"Monday": 1, "Tuesday": 2, "Wednesday": 3, "Thursday": 4,
               "Friday": 5, "Saturday": 6, "Sunday": 0}
    day_num  = day_map.get(cfg.get("weekly_trend_day", "Monday"), 1)
    h, m     = cfg.get("weekly_trend_time", "08:00").split(":")
    schedule = f"{m} {h} * * {day_num}"
    try:
        result = subprocess.run(
            ["openclaw", "cron", "add",
             "--name", "lab-field-trend-weekly",
             "--schedule", schedule,
             "--skill", "lab-field-trend",
             "--args", "--no-interactive"],
            capture_output=True, text=True, timeout=10
        )
        if result.returncode == 0:
            print(f"  ✅ Weekly trend cron: {cfg['weekly_trend_day']}s at {cfg['weekly_trend_time']}")
        else:
            raise Exception(result.stderr.strip())
    except Exception as e:
        print(f"  ⚠️  Cron registration failed: {e}")
        print(f"     Add manually: openclaw cron add --name lab-field-trend-weekly --schedule '{schedule}' --skill lab-field-trend")


# ─── Collection helpers ───────────────────────────────────────────────────────

def ask_list(question: str, default: str = "") -> list[str]:
    """checkpoint() returning a comma-split list."""
    try:
        raw = checkpoint(question, default=default, emoji="📋")
    except CheckpointAborted:
        raw = default
    return [x.strip() for x in raw.split(",") if x.strip()]


def collect_identity() -> dict:
    section_header("── Identity")
    try:
        name = checkpoint("Your name (for personalization)", default="Researcher", emoji="👤")
    except CheckpointAborted:
        name = "Researcher"
    try:
        stage = checkpoint(
            "Career stage?",
            options=["undergrad", "masters", "phd", "postdoc", "faculty", "independent"],
            default="postdoc", allow_freetext=False, emoji="🎓",
        )
    except CheckpointAborted:
        stage = "postdoc"
    fields      = ask_list("Primary research field(s) (comma-separated)", "neuroscience, machine learning")
    disciplines = ask_list("Disciplines spanned", "biomedical, computational")
    return {"user": name, "career_stage": stage, "fields": fields, "disciplines": disciplines}


def collect_tools() -> dict:
    section_header("── Knowledge Store & Tools")

    obsidian = None
    notion_db = None

    try:
        store = checkpoint(
            "Knowledge store?",
            options=["obsidian", "notion", "none"],
            default="obsidian", allow_freetext=False, emoji="📂",
        )
    except CheckpointAborted:
        store = "obsidian"

    if store == "obsidian":
        try:
            action = checkpoint(
                "Obsidian: create new vault or use existing?",
                options=["new", "existing"],
                default="new", allow_freetext=False, emoji="📁",
            )
        except CheckpointAborted:
            action = "existing"
        default_path = str(Path.home() / "LabOS-Vault")
        try:
            obsidian = checkpoint(
                "Vault path?",
                default=default_path, emoji="📁",
            )
        except CheckpointAborted:
            obsidian = default_path

    elif store == "notion":
        print("  → Go to notion.so → create a 'Papers' database → copy the database ID")
        try:
            notion_db = checkpoint("Notion database ID (or Enter to skip)", default="", emoji="📝")
        except CheckpointAborted:
            notion_db = ""
        notion_db = notion_db or None

    # Zotero
    zotero_type = None
    zotero_lib  = None
    try:
        use_zotero = confirm("Do you use Zotero?", default=False)
    except CheckpointAborted:
        use_zotero = False
    if use_zotero:
        try:
            zotero_type = checkpoint("Zotero type?", options=["local", "web"],
                                     default="web", allow_freetext=False, emoji="📚")
        except CheckpointAborted:
            zotero_type = "web"
        if zotero_type == "web":
            try:
                zotero_lib = checkpoint("Zotero library ID?", default="", emoji="📚") or None
            except CheckpointAborted:
                zotero_lib = None

    # Databases
    db_raw = ask_list(
        "Literature databases (pubmed, openalex, arxiv, semanticscholar, biorxiv)",
        "pubmed, openalex, arxiv"
    )
    valid  = {"pubmed", "openalex", "arxiv", "semanticscholar", "biorxiv"}
    dbs    = [d.lower() for d in db_raw if d.lower() in valid] or ["pubmed", "openalex", "arxiv"]

    return {
        "knowledge_store":    store,
        "obsidian_vault":     obsidian,
        "notion_research_db": notion_db,
        "zotero_type":        zotero_type,
        "zotero_library_id":  zotero_lib,
        "databases":          dbs,
    }


def collect_prefs() -> dict:
    section_header("── Preferences")
    try:
        writing_style = checkpoint(
            "Writing style preference?",
            default="concise, methods-forward, active voice", emoji="✍️",
        )
    except CheckpointAborted:
        writing_style = "concise, methods-forward, active voice"
    try:
        citation = checkpoint(
            "Citation format?",
            options=["APA", "Vancouver", "Nature", "Chicago", "MLA"],
            default="APA", allow_freetext=False, emoji="📚",
        )
    except CheckpointAborted:
        citation = "APA"
    try:
        summary = checkpoint(
            "Paper summary style?",
            options=["bullet", "paragraph", "detailed"],
            default="bullet", allow_freetext=False, emoji="📋",
        )
    except CheckpointAborted:
        summary = "bullet"
    try:
        batch_raw = checkpoint("Papers per batch to review?", default="5", emoji="📄")
        batch = int(batch_raw)
    except (CheckpointAborted, ValueError):
        batch = 5

    return {
        "writing_style":   writing_style,
        "citation_format": citation,
        "summary_style":   summary,
        "papers_per_batch": batch,
    }


