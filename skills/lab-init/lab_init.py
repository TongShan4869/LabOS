def cmd_init():
    # Already initialised?
    if CONFIG_FILE.exists():
        try:
            existing = json.loads(CONFIG_FILE.read_text())
        except Exception:
            existing = None
        if existing:
            print(f"\n🧪 LabOS already set up for {existing.get('user','?')}.")
            try:
                action = checkpoint(
                    "What do you want to do?",
                    options=["add-project", "update-prefs", "status", "reset"],
                    default="add-project", allow_freetext=False, emoji="🔀",
                )
            except CheckpointAborted:
                return
            if action == "add-project":
                cmd_add_project()
            elif action == "update-prefs":
                cmd_update_prefs()
            elif action == "status":
                cmd_status()
            elif action == "reset":
                cmd_reset()
            return

    print("\n🧪 Welcome to LabOS — setting up your virtual research lab.\n")

    identity      = collect_identity()
    tools         = collect_tools()
    prefs         = collect_prefs()
    notifications = collect_notifications()
    project       = collect_project()

    cfg = {
        **identity, **tools, **prefs, **notifications,
        "labos_version": "0.2",
        "created": now_iso(),
    }

    section_header("── Creating files")
    SESSIONS_DIR.mkdir(parents=True, exist_ok=True)
    write_config(cfg)
    write_memory(cfg, project)
    write_graph(cfg, project)
    xp_data = init_xp(cfg["user"])
    print(f"  ✅ xp.json  (Level 1 — Confused First-Year 🧪, 100 XP)")

    # Obsidian scaffold
    obsidian_ok = False
    if cfg.get("obsidian_vault"):
        obsidian_ok = scaffold_obsidian(cfg["obsidian_vault"], project)

    # Security baseline — run lab-security audit silently
    progress("Running security baseline…", "🔒")
    try:
        subprocess.run(
            [sys.executable,
             str(Path(__file__).parent / "lab-security" / "lab_security.py"),
             "--mode", "audit"],
            capture_output=True, timeout=15
        )
        print("  ✅ Security baseline complete")
    except Exception:
        print("  ⚠️  lab-security not available — run manually: lab-security --mode audit")

    # Cron
    if cfg["notify_channel"] != "none":
        register_cron(cfg)

    # Summary
    print("\n" + "═"*60)
    print(f"  ✅ LabOS initialized — {cfg['user']}")
    print("═"*60)
    print(f"\n👤 {cfg['user']} ({cfg['career_stage']})")
    print(f"🔬 Fields: {', '.join(cfg['fields'])}")
    print(f"📚 Vault: {cfg.get('obsidian_vault') or 'not set'}")
    print(f"📊 Databases: {', '.join(cfg['databases'])}")
    if project:
        print(f"📁 First project: {project['name']}")
    print(f"\n🏆 XP: 100 | Level 1 — Confused First-Year 🧪")
    print(f"📅 Weekly digest: {cfg['weekly_trend_day']}s at {cfg['weekly_trend_time']} → {cfg['notify_channel']}")
    print(f"\n💡 Try:\n"
          f"   'What's new in my field this week?'   → lab-field-trend\n"
          f"   'Find papers on [your topic]'          → lab-lit-scout\n"
          f"   'Advise me on my project'              → lab-research-advisor")

    log_session("lab-init", cfg["user"], f"Lab initialized. Project: {project['name'] if project else 'none'}")


def cmd_add_project(project: dict | None = None):
    if not CONFIG_FILE.exists():
        print("❌ No LAB_CONFIG.json. Run lab-init first.")
        sys.exit(1)
    cfg = json.loads(CONFIG_FILE.read_text())

    if project is None:
        section_header("── Add New Project")
        project = collect_project()
        if not project:
            print("Cancelled.")
            return

    _append_project_node(project)
    print(f"  ✅ Added to research-graph.jsonl")

    # Update LAB_MEMORY
    if MEMORY_FILE.exists():
        mem = MEMORY_FILE.read_text()
        entry = f"- **{project['name']}:** {project['description']} — Status: {project['status']}\n"
        mem = mem.replace(
            "(none yet — add with: `lab-init --add-project`)\n", ""
        ).replace("## Active Projects\n", f"## Active Projects\n{entry}")
        MEMORY_FILE.write_text(mem)
        print("  ✅ LAB_MEMORY.md updated")

    # Obsidian folder
    if cfg.get("obsidian_vault"):
        vault = Path(cfg["obsidian_vault"])
        if vault.exists():
            slug     = slugify(project["name"])
            proj_dir = vault / "Research" / "Projects" / slug
            proj_dir.mkdir(parents=True, exist_ok=True)
            (proj_dir / "hypotheses.md").write_text(
                f"# Hypotheses — {project['name']}\n\n"
                + (f"- {project['hypothesis']}\n" if project.get("hypothesis") else "")
            )
            (proj_dir / "notes.md").write_text(f"# Notes — {project['name']}\n\n{project['description']}\n")
            (proj_dir / "drafts").mkdir(exist_ok=True)
            print(f"  ✅ Obsidian: Research/Projects/{slug}/")

    award_xp(50, None)
    print(f"\n✅ Project '{project['name']}' added! +50 XP 🎉")


def cmd_update_prefs():
    if not CONFIG_FILE.exists():
        print("❌ No LAB_CONFIG.json. Run lab-init first.")
        sys.exit(1)
    cfg  = json.loads(CONFIG_FILE.read_text())
    prefs = collect_prefs()
    notif = collect_notifications()
    cfg.update({**prefs, **notif})
    CONFIG_FILE.write_text(json.dumps(cfg, indent=2))
    print("\n✅ Preferences updated.")


def cmd_status():
    if not CONFIG_FILE.exists():
        print("❌ No lab found. Run lab-init to get started.")
        sys.exit(1)
    cfg = json.loads(CONFIG_FILE.read_text())
    xp_data = {}
    if XP_FILE.exists():
        xp_data = json.loads(XP_FILE.read_text())

    xp      = xp_data.get("xp", 0)
    level   = xp_data.get("level", 1)
    title   = xp_data.get("level_title", "Confused First-Year")
    badges  = " ".join(xp_data.get("badges", []))

    # Count projects from graph
    n_projects = 0
    n_papers   = 0
    if GRAPH_FILE.exists():
        for line in GRAPH_FILE.read_text().splitlines():
            try:
                rec = json.loads(line)
                if rec.get("type") == "Project": n_projects += 1
                if rec.get("type") == "Paper":   n_papers   += 1
            except Exception:
                pass

    section_header("🔬 LabOS Status")
    print(f"\n👤 {cfg['user']} ({cfg['career_stage']})")
    print(f"🔬 {', '.join(cfg['fields'])}")
    print(f"🏆 Level {level} — {title} | XP: {xp}")
    if badges:
        print(f"🏅 {badges}")
    print(f"\n📁 Projects: {n_projects}  |  📄 Papers: {n_papers}")
    print(f"📚 Vault: {cfg.get('obsidian_vault') or 'not set'}")
    print(f"📊 Databases: {', '.join(cfg['databases'])}")
    print(f"📅 Weekly digest: {cfg['weekly_trend_day']}s at {cfg['weekly_trend_time']} → {cfg['notify_channel']}")
    print(f"\n📄 Config:  {CONFIG_FILE}")
    print(f"🧠 Memory:  {MEMORY_FILE}")
    print(f"🗂️  Graph:   {GRAPH_FILE}")


def cmd_reset():
    print("⚠️  This will delete ALL LabOS data (config, memory, projects, XP).")
    try:
        go = confirm("Are you sure?", default=False)
    except CheckpointAborted:
        go = False
    if not go:
        print("Cancelled.")
        return
    for f in [CONFIG_FILE, MEMORY_FILE, GRAPH_FILE, XP_FILE]:
        if f.exists():
            f.unlink()
            print(f"  ✗ Deleted {f.name}")
    print("\nLab reset. Run lab-init to start fresh.")


# ─── Main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="LabOS — virtual research lab setup")
    parser.add_argument("--add-project",  action="store_true")
    parser.add_argument("--update-prefs", action="store_true")
    parser.add_argument("--status",       action="store_true")
    parser.add_argument("--reset",        action="store_true")
    # Non-interactive project creation (used by AI agents)
    parser.add_argument("--name",        help="Project name")
    parser.add_argument("--desc",        help="Project description")
    parser.add_argument("--hypothesis",  default="")
    parser.add_argument("--proj-status", default="active",
                        choices=["idea","active","writing","submitted","published"])
    args = parser.parse_args()

    # Non-interactive AI-mediated project add
    if args.name and args.desc:
        cmd_add_project({
            "name": args.name, "description": args.desc,
            "hypothesis": args.hypothesis, "status": args.proj_status,
        })
    elif args.add_project:
        cmd_add_project()
    elif args.update_prefs:
        cmd_update_prefs()
    elif args.status:
        cmd_status()
    elif args.reset:
        cmd_reset()
    else:
        cmd_init()


if __name__ == "__main__":
    with AgentWorking("main", "Initializing lab..."):
        main()
